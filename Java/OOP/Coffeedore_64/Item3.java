public class Item3 {
    private String name;
    private double price;
    private int index;

    // Constructor

    public Item3(String name, double price) {
        this.name = name;
        this.price = price;
    }

    // Name Getter
    public String getName() {
        return name;
    }

    // Name Setter
    public void setName(String name) {
        this.name = name;

    }

    // Price Getter
    public double getPrice() {
        return price;
    }

    // Price Setter
    public void setPrice(double price) {
        this.price = price;

    }

    // Index Getter
    public int getIndex() {
        return index;
    }

    // Index Setter
    public void setIndex(int index) {
        this.index = index;

    }
}
